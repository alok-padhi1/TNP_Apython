# --- GIT COMMANDS GUIDE ---
# Use these commands in your terminal (PowerShell or Bash) to save and push your work.

# 1. Stage your changes (Preparations)
# This adds all modified files to the "staging area".
# git add .

# 2. Commit your changes (Save)
# This creates a local snapshot of your work with a message.
# git commit -m "Your descriptive message here"

# 3. Push to GitHub (Upload)
# This sends your local changes to the remote repository.
# git push origin main

# --- ADDITIONAL HELPFUL COMMANDS ---

# Check the current status of your files:
# git status

# See where your code is being pushed to:
# git remote -v

# Update your local code from GitHub (do this before pushing if you get errors):
# git pull origin main

# --- HOW IT WORKS ---
# 'origin' is the nickname for your repo: https://github.com/alok-padhi1/TNP_Apython.git
# 'main' is the name of your primary branch.
